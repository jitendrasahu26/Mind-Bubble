    private void btnNextActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNextActionPerformed
        if(validateInput()==false)
        {
            JOptionPane.showMessageDialog(null,"please fill all the inputs ", "Message",JOptionPane.INFORMATION_MESSAGE);
        }
        else
        {
            String optionName=option.get(correctOption);
            Questions obj=new Questions(newExam.getExamid(),newExam.getLanguage(),option1,option2,option3,option4,qno,optionName,question); 
            qStore.addQuestion(obj);
            clearAll();
            lblTitle.setText((newExam.getTotalQuestion()-qno)+":");
            qno++;
            if(qno>newExam.getTotalQuestion())
            {
                dissable();
                JOptionPane.showMessageDialog(null," All Question are filled ", "Message",JOptionPane.INFORMATION_MESSAGE);
            }
            else
            {
                lblQno.setText("Queston "+qno+":");
            }
        }
    }//GEN-LAST:event_btnNextActionPerformed

