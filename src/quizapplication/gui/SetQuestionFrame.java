    private void BtnDoneActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnDoneActionPerformed
        if(qno>newExam.getTotalQuestion())
        {   try
            {
                ExamDAO.addExam(newExam);
                QuestionsDAO.addQuestion(qStore);
                JOptionPane.showMessageDialog(null," data entered successfully ","information",JOptionPane.INFORMATION_MESSAGE);
                AdminOption ao=new AdminOption();
                ao.setVisible(true);
                this.dispose();
            }
            catch(SQLException e)
            {
                JOptionPane.showMessageDialog(null,"SQLException "+e, "Error",JOptionPane.ERROR_MESSAGE);
                e.printStackTrace();
            }
        }
        else{
            JOptionPane.showMessageDialog(null,"fill All The Question ", "Message",JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_BtnDoneActionPerformed

