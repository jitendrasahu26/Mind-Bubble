    private void btnDoneActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDoneActionPerformed
    String chosenAnswer=getUserAnswer();
    if(chosenAnswer!=null)
        {
            Questions question=qStore.getQuestion(pos);
            String correctAnswer=question.getCorrect_answer();
            Answerpojo newans=new Answerpojo(exam.getExamid(),exam.getLanguage(),qno,chosenAnswer,correctAnswer);
            System.out.println("newans "+newans);
            Answerpojo answer=aStore.getAnswerByQno(qno);
            if(answer==null)
            {
                aStore.addAnswer(newans);
            }
            else{
                if(newans.getChoosenAnswer().equals(answer.getChoosenAnswer())==false)
                {
                    int apos=aStore.removeAnswer(answer);
                    aStore.setAnswerAt(apos,newans);
                }
            }
        }
    for(Questions question:qStore.getAllQuestion())
    {
        int qno=question.getQno();
        Answerpojo  answer=aStore.getAnswerByQno(qno);
        if(answer==null)
         continue;
        else
        {
            String chosenAnswer1=answer.getChoosenAnswer().trim();
            System.out.println(chosenAnswer1);
            String correctAnswer=answer.getCorrectAnswer().trim();
            System.out.println(correctAnswer);
            if(chosenAnswer1.equals(correctAnswer)==true)
            {
                System.out.println("inside if");
                right++;
            }
            else
            {
                System.out.println("inside else");
                wrong++;
            }
        }
    }
        String reportcard="total Question "+qStore.getCount();
        reportcard+="\nRight Answer :"+right;
        reportcard+="\nwrong Answer :"+wrong;
        reportcard+="\nUnAttempted:"+(qStore.getCount()-(right+wrong));
        JOptionPane.showMessageDialog(null,reportcard," reportcard",JOptionPane.INFORMATION_MESSAGE);
        double per=right*100/qStore.getCount();
          JOptionPane.showMessageDialog(null,"You scored "+per+" in your test","Result",JOptionPane.INFORMATION_MESSAGE);
        int unAttempted=qStore.getCount()-(right+wrong);
        try
        {
            Performance perofrmance=new Performance(UserProfile.getUsername(),exam.getExamid(),right,wrong,unAttempted,per,exam.getLanguage());
            PerformanceDAO.addPerfroamnce(perofrmance);
            ChoosePaperframe cpf=new ChoosePaperframe();
            cpf.setVisible(true);
            this.dispose();
        }
        catch(SQLException e)
        {
           JOptionPane.showMessageDialog(null,"SQLEXception "+e," error",JOptionPane.ERROR_MESSAGE);
           e.printStackTrace();
        }
    }//GEN-LAST:event_btnDoneActionPerformed

