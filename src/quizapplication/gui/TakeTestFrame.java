        jrOption2.setText(question.getAnswer2());
        jrOption3.setText(question.getAnswer3());
        jrOption4.setText(question.getAnswer4());
        Answerpojo answer=aStore.getAnswerByQno(qno);
        if(answer==null)
            return;
